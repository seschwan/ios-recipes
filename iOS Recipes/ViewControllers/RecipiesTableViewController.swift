    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "Cell", for: indexPath)

        // Configure the cell...
        let recipe = recipes[indexPath.row]
        cell.textLabel?.text = recipe.name
    
        return cell
    }
    
    override func prepare(for segue: UIStoryboardSegue, sender: Any?) {
        if segue.identifier == "cellDetailSegue" {
            
            // Making the connection to the segue
            guard let destinationVC = segue.destination as? RecipeDetailViewController else { return }
           
            // Creating a constant to identify what cell was tapped.
            guard let indexPath = tableView.indexPathForSelectedRow else { return }
            
            // Getting the recipes array info
            let recipe = recipes[indexPath.row]
            
            // Passing the info on..
            destinationVC.recipe = recipe
        }
    }

